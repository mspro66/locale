   /**
    * Get the description of language associated to locale identifier.
    *
    * @param string $lc_identifier Locale identifier to search.
    *
    * @return false|string Return False if locale identifier is not valid, return string with description of identifier.
    */
   function getLanguageDescription (string $lc_identifier): false|string
   {
      if (!$this->isValid($lc_identifier))
         return false;

      return self::repository[$lc_identifier][0];
   }

   /**
    * Verify if locale identifier is valid.
    *
    * @param $lc_identifier string Identifier to verify.
    *
    * @return bool True if is a valid identifier, False otherwise.
    */
   function isValid (string $lc_identifier): bool
   {
      if (!array_key_exists($lc_identifier, self::repository))
         return false;

      return true;
   }

   /**
    * Return a list of locale language associate specifically to countri iso code passed.
    *
    * @param string $iso_country Standard 2-character iso country code
    *
    * @return false|string Return False if no locale identifier founded, an array with all country language identifier and descriptions.
    */
   function getIdentifierOfCountry (string $iso_country): false|array
   {
      $iso_country = strtoupper($iso_country);
      $resA        = array();

      foreach (self::repository as $key => $value)
      {
         if ($iso_country != $value[1])
            continue;

         $resA[$iso_country][] = ['id' => $key, 'dsc' => $value[0], 'std' => $value[2]];
      }

      if (empty($resA))
         return false;
      else
         return $resA;
   }
}

